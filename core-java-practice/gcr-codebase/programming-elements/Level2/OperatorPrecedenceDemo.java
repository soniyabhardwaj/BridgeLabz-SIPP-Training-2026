// Program to demonstrate Operator Precedence

public class OperatorPrecedenceDemo {
    public static void main(String[] args) {

        // Variables
        int number1 = 10;
        int number2 = 5;
        int number3 = 2;

        // Expression
        int result = number1 + number2 * number3;

        // Display Result
        System.out.println("Result = " + result);
    }
}